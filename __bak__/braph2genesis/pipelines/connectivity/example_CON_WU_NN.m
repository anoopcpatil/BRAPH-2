%EXAMPLE_CON_WU_NN
% Script example pipeline CON WU NN

clear variables %#ok<*NASGU>

%% Load BrainAtlas
im_ba = ImporterBrainAtlasXLS('FILE', [fileparts(which('example_CON_WU')) filesep 'example data CON (DTI)' filesep 'desikan_atlas.xlsx']);

ba = im_ba.get('BA');

%% Load Groups of SubjectCON
im_gr1 = ImporterGroupSubjectCONXLS( ...
    'DIRECTORY', [fileparts(which('example_CON_WU')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba ...
    );

gr1 = im_gr1.get('GR');

im_gr2 = ImporterGroupSubjectCONXLS( ...
    'DIRECTORY', [fileparts(which('example_CON_WU')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName2'], ...
    'BA', ba ...
    );

gr2 = im_gr2.get('GR');

%% Analysis CON WU
a_WU1 = AnalyzeEnsemble_CON_WU( ...
    'GR', gr1 ...
    );

a_WU2 = AnalyzeEnsemble_CON_WU( ...
    'GR', gr2 ...
    );

%% Setup NN parameters
nn_con_classifier = ClassifierNN_CON( ...
    'A_1', a_WU1, ...
    'A_2', a_WU2, ...
    'KFOLD', 5, ...
    'REPETITION', 2, ...
    'DENSITY_OF_FEATURE_SELECTION', 0.05);

% nn result calculation
nn_con_classifier.memorize('NEURAL_NETWORK_ANALYSIS');
nn_con_classifier.get_ROC_curves();
nn_con_classifier.get_weighted_connection_mask();
nn_con_classifier.get_confusion_matrix_and_scores();