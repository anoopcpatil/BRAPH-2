%TEST_MULTIPLEXWD

%% Test 1.1: Instantiation - empty

g = MultiplexWD();

prop_number = MultiplexWD.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(MultiplexWD.getPropTag(prop));
	assert( ...
		isa(g.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexWD:' BRAPH2.BUG_FUNC], ...
		['MultiplexWD.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexWD.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(g.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexWD:' BRAPH2.BUG_FUNC], ...
		['MultiplexWD.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexWD.getr(''' TAG ''').'] ...
		)
end

