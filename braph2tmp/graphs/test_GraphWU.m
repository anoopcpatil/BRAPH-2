	GraphWU.B, GraphWU.getPropDefault(GraphWU.B), ...
	GraphWU.ATTEMPTSPEREDGE, GraphWU.getPropDefault(GraphWU.ATTEMPTSPEREDGE), ...
	GraphWU.NUMBEROFWEIGHTS, GraphWU.getPropDefault(GraphWU.NUMBEROFWEIGHTS) ...
	);
warning('on', 'BRAPH2:GraphWU')

prop_number = GraphWU.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(GraphWU.getPropTag(prop));
	switch GraphWU.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(g.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
				['GraphWU.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(g.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
				['GraphWU.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(g.getPropSettings(prop))
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop)), ...
					[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
					['GraphWU.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop), g.getPropSettings(prop)), ...
					[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
					['GraphWU.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

