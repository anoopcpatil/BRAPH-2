%TEST_GRAPHWU

%% Test 1.1: Instantiation - empty

g = GraphWU();

prop_number = GraphWU.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(GraphWU.getPropTag(prop));
	assert( ...
		isa(g.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
		['GraphWU.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in GraphWU.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(g.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':GraphWU:' BRAPH2.BUG_FUNC], ...
		['GraphWU.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in GraphWU.getr(''' TAG ''').'] ...
		)
end

