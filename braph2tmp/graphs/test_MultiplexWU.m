				~isa(g.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':MultiplexWU:' BRAPH2.BUG_FUNC], ...
				['MultiplexWU.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(g.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':MultiplexWU:' BRAPH2.BUG_FUNC], ...
				['MultiplexWU.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(g.getPropSettings(prop))
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop)), ...
					[BRAPH2.STR ':MultiplexWU:' BRAPH2.BUG_FUNC], ...
					['MultiplexWU.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop), g.getPropSettings(prop)), ...
					[BRAPH2.STR ':MultiplexWU:' BRAPH2.BUG_FUNC], ...
					['MultiplexWU.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

