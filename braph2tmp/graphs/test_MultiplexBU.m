%TEST_MULTIPLEXBU

%% Test 1.1: Instantiation - empty

g = MultiplexBU();

prop_number = MultiplexBU.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(MultiplexBU.getPropTag(prop));
	assert( ...
		isa(g.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexBU:' BRAPH2.BUG_FUNC], ...
		['MultiplexBU.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexBU.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(g.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexBU:' BRAPH2.BUG_FUNC], ...
		['MultiplexBU.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexBU.getr(''' TAG ''').'] ...
		)
end

