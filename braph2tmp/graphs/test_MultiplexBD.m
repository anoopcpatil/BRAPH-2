%TEST_MULTIPLEXBD

%% Test 1.1: Instantiation - empty

g = MultiplexBD();

prop_number = MultiplexBD.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(MultiplexBD.getPropTag(prop));
	assert( ...
		isa(g.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexBD:' BRAPH2.BUG_FUNC], ...
		['MultiplexBD.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexBD.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(g.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':MultiplexBD:' BRAPH2.BUG_FUNC], ...
		['MultiplexBD.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in MultiplexBD.getr(''' TAG ''').'] ...
		)
end

