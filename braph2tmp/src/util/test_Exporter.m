%TEST_EXPORTER

%% Test 1.1: Instantiation - empty

ex = Exporter();

prop_number = Exporter.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Exporter.getPropTag(prop));
	assert( ...
		isa(ex.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':Exporter:' BRAPH2.BUG_FUNC], ...
		['Exporter.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Exporter.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(ex.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':Exporter:' BRAPH2.BUG_FUNC], ...
		['Exporter.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Exporter.getr(''' TAG ''').'] ...
		)
end

