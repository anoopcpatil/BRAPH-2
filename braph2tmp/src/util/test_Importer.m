%TEST_IMPORTER

%% Test 1.1: Instantiation - empty

im = Importer();

prop_number = Importer.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Importer.getPropTag(prop));
	assert( ...
		isa(im.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
		['Importer.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Importer.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(im.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
		['Importer.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Importer.getr(''' TAG ''').'] ...
		)
end

