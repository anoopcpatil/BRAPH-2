				'given by Importer.getPropDefaultConditioned(' int2str(prop) '). ' ...
				'Or there could be an error in Importer.getr(''' TAG ''').'] ...
				)
		case Category.RESULT
			assert( ...
				isa(im.getr(prop), 'NoValue'), ...
				[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
				['Being a result, Importer.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
