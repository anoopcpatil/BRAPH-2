%% Test 3: Result

warning('off', 'BRAPH2:Importer')
im = Importer( ...
	Importer.ID, Importer.getPropDefault(Importer.ID), ...
	Importer.LABEL, Importer.getPropDefault(Importer.LABEL), ...
	Importer.NOTES, Importer.getPropDefault(Importer.NOTES), ...
	Importer.WAITBAR, Importer.getPropDefault(Importer.WAITBAR) ...
	);
warning('on', 'BRAPH2:Importer')

prop_number = Importer.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Importer.getPropTag(prop));
	switch Importer.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(im.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
				['Importer.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(im.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
				['Importer.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(im.getPropSettings(prop))
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop)), ...
					[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
					['Importer.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop), im.getPropSettings(prop)), ...
					[BRAPH2.STR ':Importer:' BRAPH2.BUG_FUNC], ...
					['Importer.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

