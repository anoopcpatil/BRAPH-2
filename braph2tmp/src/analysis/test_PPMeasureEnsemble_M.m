%% Test 1.1: Instantiation - empty

pr = PPMeasureEnsemble_M();

prop_number = PPMeasureEnsemble_M.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPMeasureEnsemble_M.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PPMeasureEnsemble_M:' BRAPH2.BUG_FUNC], ...
		['PPMeasureEnsemble_M.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
