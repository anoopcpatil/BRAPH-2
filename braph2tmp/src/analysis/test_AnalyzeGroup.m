%TEST_ANALYZEGROUP

%% Test 1.1: Instantiation - empty

a = AnalyzeGroup();

prop_number = AnalyzeGroup.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(AnalyzeGroup.getPropTag(prop));
	assert( ...
		isa(a.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
		['AnalyzeGroup.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in AnalyzeGroup.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(a.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
		['AnalyzeGroup.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in AnalyzeGroup.getr(''' TAG ''').'] ...
		)
end

