%% Test 3: Result

warning('off', 'BRAPH2:AnalyzeGroup')
a = AnalyzeGroup( ...
	AnalyzeGroup.ID, AnalyzeGroup.getPropDefault(AnalyzeGroup.ID), ...
	AnalyzeGroup.LABEL, AnalyzeGroup.getPropDefault(AnalyzeGroup.LABEL), ...
	AnalyzeGroup.NOTES, AnalyzeGroup.getPropDefault(AnalyzeGroup.NOTES), ...
	AnalyzeGroup.GR, AnalyzeGroup.getPropDefault(AnalyzeGroup.GR), ...
	AnalyzeGroup.G, AnalyzeGroup.getPropDefault(AnalyzeGroup.G) ...
	);
warning('on', 'BRAPH2:AnalyzeGroup')

prop_number = AnalyzeGroup.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(AnalyzeGroup.getPropTag(prop));
	switch AnalyzeGroup.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(a.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
				['AnalyzeGroup.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(a.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
				['AnalyzeGroup.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(a.getPropSettings(prop))
				assert( ...
					a.checkFormat(a.getPropFormat(prop), a.get(prop)), ...
					[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
					['AnalyzeGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					a.checkFormat(a.getPropFormat(prop), a.get(prop), a.getPropSettings(prop)), ...
					[BRAPH2.STR ':AnalyzeGroup:' BRAPH2.BUG_FUNC], ...
					['AnalyzeGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

