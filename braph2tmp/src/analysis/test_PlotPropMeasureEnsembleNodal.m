				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureEnsembleNodal:' BRAPH2.BUG_FUNC], ...
					['PlotPropMeasureEnsembleNodal.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureEnsembleNodal:' BRAPH2.BUG_FUNC], ...
