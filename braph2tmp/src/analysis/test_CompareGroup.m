				~isa(c.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
				['CompareGroup.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(c.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
				['CompareGroup.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(c.getPropSettings(prop))
				assert( ...
					c.checkFormat(c.getPropFormat(prop), c.get(prop)), ...
					[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
					['CompareGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					c.checkFormat(c.getPropFormat(prop), c.get(prop), c.getPropSettings(prop)), ...
					[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
					['CompareGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

