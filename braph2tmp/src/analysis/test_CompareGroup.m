%TEST_COMPAREGROUP

%% Test 1.1: Instantiation - empty

c = CompareGroup();

prop_number = CompareGroup.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(CompareGroup.getPropTag(prop));
	assert( ...
		isa(c.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
		['CompareGroup.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in CompareGroup.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(c.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':CompareGroup:' BRAPH2.BUG_FUNC], ...
		['CompareGroup.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in CompareGroup.getr(''' TAG ''').'] ...
		)
end

