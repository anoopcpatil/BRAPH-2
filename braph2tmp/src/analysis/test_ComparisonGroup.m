prop_number = ComparisonGroup.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ComparisonGroup.getPropTag(prop));
	switch ComparisonGroup.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(cp.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
				['ComparisonGroup.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(cp.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
				['ComparisonGroup.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(cp.getPropSettings(prop))
				assert( ...
					cp.checkFormat(cp.getPropFormat(prop), cp.get(prop)), ...
					[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
					['ComparisonGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					cp.checkFormat(cp.getPropFormat(prop), cp.get(prop), cp.getPropSettings(prop)), ...
					[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
					['ComparisonGroup.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

