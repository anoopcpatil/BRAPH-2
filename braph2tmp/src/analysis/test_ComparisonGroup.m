%TEST_COMPARISONGROUP

%% Test 1.1: Instantiation - empty

cp = ComparisonGroup();

prop_number = ComparisonGroup.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ComparisonGroup.getPropTag(prop));
	assert( ...
		isa(cp.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
		['ComparisonGroup.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ComparisonGroup.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(cp.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':ComparisonGroup:' BRAPH2.BUG_FUNC], ...
		['ComparisonGroup.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ComparisonGroup.getr(''' TAG ''').'] ...
		)
end

