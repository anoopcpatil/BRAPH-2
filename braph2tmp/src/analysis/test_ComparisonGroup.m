	ComparisonGroup.P1, ComparisonGroup.getPropDefault(ComparisonGroup.P1), ...
	ComparisonGroup.P2, ComparisonGroup.getPropDefault(ComparisonGroup.P2), ...
	ComparisonGroup.CIL, ComparisonGroup.getPropDefault(ComparisonGroup.CIL), ...
	ComparisonGroup.CIU, ComparisonGroup.getPropDefault(ComparisonGroup.CIU) ...
	);
