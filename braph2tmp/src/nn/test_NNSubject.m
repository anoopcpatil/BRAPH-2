			assert( ...
				~isa(sub.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
				['NNSubject.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(sub.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
				['NNSubject.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(sub.getPropSettings(prop))
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop)), ...
					[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
					['NNSubject.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop), sub.getPropSettings(prop)), ...
					[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
					['NNSubject.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
