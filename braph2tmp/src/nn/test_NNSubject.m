%TEST_NNSUBJECT

%% Test 1.1: Instantiation - empty

sub = NNSubject();

prop_number = NNSubject.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(NNSubject.getPropTag(prop));
	assert( ...
		isa(sub.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
		['NNSubject.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in NNSubject.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(sub.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':NNSubject:' BRAPH2.BUG_FUNC], ...
		['NNSubject.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in NNSubject.getr(''' TAG ''').'] ...
		)
end

