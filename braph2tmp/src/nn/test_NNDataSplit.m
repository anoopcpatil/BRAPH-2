	NNDataSplit.FEATURE_SELECTION_ANALYSIS, NNDataSplit.getPropDefault(NNDataSplit.FEATURE_SELECTION_ANALYSIS), ...
	NNDataSplit.GR_TRAIN_FS, NNDataSplit.getPropDefault(NNDataSplit.GR_TRAIN_FS), ...
	NNDataSplit.GR_VAL_FS, NNDataSplit.getPropDefault(NNDataSplit.GR_VAL_FS) ...
	);
warning('on', 'BRAPH2:NNDataSplit')

prop_number = NNDataSplit.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(NNDataSplit.getPropTag(prop));
	switch NNDataSplit.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(nnds.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNDataSplit:' BRAPH2.BUG_FUNC], ...
				['NNDataSplit.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(nnds.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNDataSplit:' BRAPH2.BUG_FUNC], ...
				['NNDataSplit.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(nnds.getPropSettings(prop))
				assert( ...
					nnds.checkFormat(nnds.getPropFormat(prop), nnds.get(prop)), ...
					[BRAPH2.STR ':NNDataSplit:' BRAPH2.BUG_FUNC], ...
					['NNDataSplit.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					nnds.checkFormat(nnds.getPropFormat(prop), nnds.get(prop), nnds.getPropSettings(prop)), ...
					[BRAPH2.STR ':NNDataSplit:' BRAPH2.BUG_FUNC], ...
					['NNDataSplit.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

