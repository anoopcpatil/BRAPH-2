	methods (Access=protected) % calculate value
		function value = calculateValue(sub, prop)
		
			switch prop
				case NNSubject.MASKED_INPUT
					rng(sub.getPropSeed(NNSubject.MASKED_INPUT), 'twister')
					
					feature_mask = sub.get('FEATURE_MASK');
					if isempty(feature_mask)
					    masked_input = {};
					else
					    input = sub.get('INPUT');
					    for i = 1:1:length(feature_mask)
					        input_per_cluster = input{i};
					        mask_per_cluster = feature_mask{i};
					        input_per_cluster(mask_per_cluster == 0) = 0;
					        masked_input{i} = input_per_cluster;
					    end
					end
					
					value = masked_input;
					
				otherwise
					value = calculateValue@Subject(sub, prop);
					
			end
		end
	end
