nnd = NNData( ...
	NNData.ID, NNData.getPropDefault(NNData.ID), ...
	NNData.LABEL, NNData.getPropDefault(NNData.LABEL), ...
	NNData.NOTES, NNData.getPropDefault(NNData.NOTES), ...
