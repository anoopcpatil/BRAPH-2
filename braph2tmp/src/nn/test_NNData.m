	NNData.INPUT_TYPE, NNData.getPropDefault(NNData.INPUT_TYPE), ...
	NNData.G, NNData.getPropDefault(NNData.G), ...
	NNData.MEASURES, NNData.getPropDefault(NNData.MEASURES), ...
	NNData.TARGET_NAME, NNData.getPropDefault(NNData.TARGET_NAME), ...
	NNData.GR, NNData.getPropDefault(NNData.GR), ...
	NNData.GR_NN, NNData.getPropDefault(NNData.GR_NN) ...
	);
warning('on', 'BRAPH2:NNData')

prop_number = NNData.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(NNData.getPropTag(prop));
	switch NNData.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(nnd.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
				['NNData.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(nnd.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
				['NNData.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(nnd.getPropSettings(prop))
				assert( ...
					nnd.checkFormat(nnd.getPropFormat(prop), nnd.get(prop)), ...
					[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
					['NNData.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					nnd.checkFormat(nnd.getPropFormat(prop), nnd.get(prop), nnd.getPropSettings(prop)), ...
					[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
					['NNData.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

