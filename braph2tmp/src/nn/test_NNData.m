%TEST_NNDATA

%% Test 1.1: Instantiation - empty

nnd = NNData();

prop_number = NNData.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(NNData.getPropTag(prop));
	assert( ...
		isa(nnd.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
		['NNData.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in NNData.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(nnd.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':NNData:' BRAPH2.BUG_FUNC], ...
		['NNData.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in NNData.getr(''' TAG ''').'] ...
		)
end

