%TEST_PPNNSUBJECT_INPUT

%% Test 1.1: Instantiation - empty

pl = PPNNSubject_Input();

prop_number = PPNNSubject_Input.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPNNSubject_Input.getPropTag(prop));
	assert( ...
		isa(pl.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
		['PPNNSubject_Input.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPNNSubject_Input.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pl.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
		['PPNNSubject_Input.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPNNSubject_Input.getr(''' TAG ''').'] ...
		)
end

