	PPNNSubject_Input.ENABLE, PPNNSubject_Input.getPropDefault(PPNNSubject_Input.ENABLE), ...
	PPNNSubject_Input.WAITBAR, PPNNSubject_Input.getPropDefault(PPNNSubject_Input.WAITBAR) ...
	);
warning('on', 'BRAPH2:PPNNSubject_Input')

prop_number = PPNNSubject_Input.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPNNSubject_Input.getPropTag(prop));
	switch PPNNSubject_Input.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pl.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
				['PPNNSubject_Input.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pl.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
				['PPNNSubject_Input.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pl.getPropSettings(prop))
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop)), ...
					[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
					['PPNNSubject_Input.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop), pl.getPropSettings(prop)), ...
					[BRAPH2.STR ':PPNNSubject_Input:' BRAPH2.BUG_FUNC], ...
					['PPNNSubject_Input.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

