	PPNNBase_Model.ENABLE, PPNNBase_Model.getPropDefault(PPNNBase_Model.ENABLE), ...
	PPNNBase_Model.WAITBAR, PPNNBase_Model.getPropDefault(PPNNBase_Model.WAITBAR) ...
	);
warning('on', 'BRAPH2:PPNNBase_Model')

prop_number = PPNNBase_Model.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPNNBase_Model.getPropTag(prop));
	switch PPNNBase_Model.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPNNBase_Model:' BRAPH2.BUG_FUNC], ...
				['PPNNBase_Model.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPNNBase_Model:' BRAPH2.BUG_FUNC], ...
				['PPNNBase_Model.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PPNNBase_Model:' BRAPH2.BUG_FUNC], ...
					['PPNNBase_Model.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PPNNBase_Model:' BRAPH2.BUG_FUNC], ...
					['PPNNBase_Model.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

