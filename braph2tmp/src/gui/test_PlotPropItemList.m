		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
		['PlotPropItemList.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropItemList.getr(''' TAG ''').'] ...
		)
end

