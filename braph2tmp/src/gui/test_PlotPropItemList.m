%% Test 3: Result

warning('off', 'BRAPH2:PlotPropItemList')
pr = PlotPropItemList( ...
	PlotPropItemList.ID, PlotPropItemList.getPropDefault(PlotPropItemList.ID), ...
	PlotPropItemList.BKGCOLOR, PlotPropItemList.getPropDefault(PlotPropItemList.BKGCOLOR), ...
	PlotPropItemList.EL, PlotPropItemList.getPropDefault(PlotPropItemList.EL), ...
	PlotPropItemList.PROP, PlotPropItemList.getPropDefault(PlotPropItemList.PROP), ...
	PlotPropItemList.TITLE, PlotPropItemList.getPropDefault(PlotPropItemList.TITLE), ...
	PlotPropItemList.ENABLE, PlotPropItemList.getPropDefault(PlotPropItemList.ENABLE), ...
	PlotPropItemList.WAITBAR, PlotPropItemList.getPropDefault(PlotPropItemList.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropItemList')

prop_number = PlotPropItemList.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropItemList.getPropTag(prop));
	switch PlotPropItemList.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
				['PlotPropItemList.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
				['PlotPropItemList.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
					['PlotPropItemList.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
					['PlotPropItemList.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

