%% Test 1.1: Instantiation - empty

pr = PlotPropItemList();

prop_number = PlotPropItemList.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropItemList.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropItemList:' BRAPH2.BUG_FUNC], ...
		['PlotPropItemList.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropItemList.getr(' int2str(prop) ').'] ...
		)
