%% Test 3: Result

warning('off', 'BRAPH2:PlotPropClassList')
pr = PlotPropClassList( ...
	PlotPropClassList.ID, PlotPropClassList.getPropDefault(PlotPropClassList.ID), ...
	PlotPropClassList.BKGCOLOR, PlotPropClassList.getPropDefault(PlotPropClassList.BKGCOLOR), ...
	PlotPropClassList.EL, PlotPropClassList.getPropDefault(PlotPropClassList.EL), ...
	PlotPropClassList.PROP, PlotPropClassList.getPropDefault(PlotPropClassList.PROP), ...
	PlotPropClassList.TITLE, PlotPropClassList.getPropDefault(PlotPropClassList.TITLE), ...
	PlotPropClassList.ENABLE, PlotPropClassList.getPropDefault(PlotPropClassList.ENABLE), ...
	PlotPropClassList.WAITBAR, PlotPropClassList.getPropDefault(PlotPropClassList.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropClassList')

prop_number = PlotPropClassList.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropClassList.getPropTag(prop));
	switch PlotPropClassList.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropClassList:' BRAPH2.BUG_FUNC], ...
				['PlotPropClassList.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropClassList:' BRAPH2.BUG_FUNC], ...
				['PlotPropClassList.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropClassList:' BRAPH2.BUG_FUNC], ...
					['PlotPropClassList.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropClassList:' BRAPH2.BUG_FUNC], ...
					['PlotPropClassList.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

