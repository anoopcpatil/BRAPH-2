	PPPipeline_PSDict.ID, PPPipeline_PSDict.getPropDefault(PPPipeline_PSDict.ID), ...
	PPPipeline_PSDict.BKGCOLOR, PPPipeline_PSDict.getPropDefault(PPPipeline_PSDict.BKGCOLOR), ...
