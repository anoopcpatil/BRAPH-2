%% Test 3: Result

warning('off', 'BRAPH2:PlotPropScalar')
pl = PlotPropScalar( ...
	PlotPropScalar.ID, PlotPropScalar.getPropDefault(PlotPropScalar.ID), ...
	PlotPropScalar.BKGCOLOR, PlotPropScalar.getPropDefault(PlotPropScalar.BKGCOLOR), ...
	PlotPropScalar.EL, PlotPropScalar.getPropDefault(PlotPropScalar.EL), ...
	PlotPropScalar.PROP, PlotPropScalar.getPropDefault(PlotPropScalar.PROP), ...
	PlotPropScalar.TITLE, PlotPropScalar.getPropDefault(PlotPropScalar.TITLE), ...
	PlotPropScalar.ENABLE, PlotPropScalar.getPropDefault(PlotPropScalar.ENABLE), ...
	PlotPropScalar.WAITBAR, PlotPropScalar.getPropDefault(PlotPropScalar.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropScalar')

prop_number = PlotPropScalar.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropScalar.getPropTag(prop));
	switch PlotPropScalar.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pl.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
				['PlotPropScalar.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pl.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
				['PlotPropScalar.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pl.getPropSettings(prop))
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop)), ...
					[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
					['PlotPropScalar.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop), pl.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
					['PlotPropScalar.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

