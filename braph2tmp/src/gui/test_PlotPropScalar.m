%TEST_PLOTPROPSCALAR

%% Test 1.1: Instantiation - empty

pl = PlotPropScalar();

prop_number = PlotPropScalar.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropScalar.getPropTag(prop));
	assert( ...
		isa(pl.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
		['PlotPropScalar.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropScalar.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pl.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropScalar:' BRAPH2.BUG_FUNC], ...
		['PlotPropScalar.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropScalar.getr(''' TAG ''').'] ...
		)
end

