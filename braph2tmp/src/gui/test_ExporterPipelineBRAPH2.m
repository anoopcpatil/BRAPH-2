		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(ex.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':ExporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
				['ExporterPipelineBRAPH2.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(ex.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':ExporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
				['ExporterPipelineBRAPH2.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(ex.getPropSettings(prop))
				assert( ...
					ex.checkFormat(ex.getPropFormat(prop), ex.get(prop)), ...
					[BRAPH2.STR ':ExporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
					['ExporterPipelineBRAPH2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					ex.checkFormat(ex.getPropFormat(prop), ex.get(prop), ex.getPropSettings(prop)), ...
					[BRAPH2.STR ':ExporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
					['ExporterPipelineBRAPH2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

