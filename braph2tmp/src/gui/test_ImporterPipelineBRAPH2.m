	ImporterPipelineBRAPH2.FILE, ImporterPipelineBRAPH2.getPropDefault(ImporterPipelineBRAPH2.FILE), ...
	ImporterPipelineBRAPH2.PIP, ImporterPipelineBRAPH2.getPropDefault(ImporterPipelineBRAPH2.PIP) ...
	);
warning('on', 'BRAPH2:ImporterPipelineBRAPH2')

prop_number = ImporterPipelineBRAPH2.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ImporterPipelineBRAPH2.getPropTag(prop));
	switch ImporterPipelineBRAPH2.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(im.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':ImporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
				['ImporterPipelineBRAPH2.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(im.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':ImporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
				['ImporterPipelineBRAPH2.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(im.getPropSettings(prop))
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop)), ...
					[BRAPH2.STR ':ImporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
					['ImporterPipelineBRAPH2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop), im.getPropSettings(prop)), ...
					[BRAPH2.STR ':ImporterPipelineBRAPH2:' BRAPH2.BUG_FUNC], ...
					['ImporterPipelineBRAPH2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

