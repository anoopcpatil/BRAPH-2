%TEST_GUI

%% Test 1.1: Instantiation - empty

gui = GUI();

prop_number = GUI.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(GUI.getPropTag(prop));
	assert( ...
		isa(gui.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
		['GUI.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in GUI.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(gui.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
		['GUI.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in GUI.getr(''' TAG ''').'] ...
		)
end

