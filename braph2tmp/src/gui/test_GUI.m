	TAG = upper(GUI.getPropTag(prop));
	switch GUI.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(gui.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
				['GUI.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(gui.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
				['GUI.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(gui.getPropSettings(prop))
				assert( ...
					gui.checkFormat(gui.getPropFormat(prop), gui.get(prop)), ...
					[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
					['GUI.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					gui.checkFormat(gui.getPropFormat(prop), gui.get(prop), gui.getPropSettings(prop)), ...
					[BRAPH2.STR ':GUI:' BRAPH2.BUG_FUNC], ...
					['GUI.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

