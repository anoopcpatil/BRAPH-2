		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropSmartVector:' BRAPH2.BUG_FUNC], ...
				['PlotPropSmartVector.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropSmartVector:' BRAPH2.BUG_FUNC], ...
				['PlotPropSmartVector.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropSmartVector:' BRAPH2.BUG_FUNC], ...
					['PlotPropSmartVector.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropSmartVector:' BRAPH2.BUG_FUNC], ...
					['PlotPropSmartVector.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

