%% Test 3: Result

warning('off', 'BRAPH2:PlotPropLogical')
pr = PlotPropLogical( ...
	PlotPropLogical.ID, PlotPropLogical.getPropDefault(PlotPropLogical.ID), ...
	PlotPropLogical.BKGCOLOR, PlotPropLogical.getPropDefault(PlotPropLogical.BKGCOLOR), ...
	PlotPropLogical.EL, PlotPropLogical.getPropDefault(PlotPropLogical.EL), ...
	PlotPropLogical.PROP, PlotPropLogical.getPropDefault(PlotPropLogical.PROP), ...
	PlotPropLogical.TITLE, PlotPropLogical.getPropDefault(PlotPropLogical.TITLE), ...
	PlotPropLogical.ENABLE, PlotPropLogical.getPropDefault(PlotPropLogical.ENABLE), ...
	PlotPropLogical.WAITBAR, PlotPropLogical.getPropDefault(PlotPropLogical.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropLogical')

prop_number = PlotPropLogical.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropLogical.getPropTag(prop));
	switch PlotPropLogical.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
				['PlotPropLogical.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
				['PlotPropLogical.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
					['PlotPropLogical.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
					['PlotPropLogical.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

