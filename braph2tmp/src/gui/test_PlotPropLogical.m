%TEST_PLOTPROPLOGICAL

%% Test 1.1: Instantiation - empty

pr = PlotPropLogical();

prop_number = PlotPropLogical.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropLogical.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
		['PlotPropLogical.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropLogical.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropLogical:' BRAPH2.BUG_FUNC], ...
		['PlotPropLogical.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropLogical.getr(''' TAG ''').'] ...
		)
end

