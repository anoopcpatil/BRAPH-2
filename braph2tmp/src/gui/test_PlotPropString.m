warning('off', 'BRAPH2:PlotPropString')
pr = PlotPropString( ...
	PlotPropString.ID, PlotPropString.getPropDefault(PlotPropString.ID), ...
	PlotPropString.BKGCOLOR, PlotPropString.getPropDefault(PlotPropString.BKGCOLOR), ...
	PlotPropString.EL, PlotPropString.getPropDefault(PlotPropString.EL), ...
