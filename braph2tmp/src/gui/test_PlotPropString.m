%TEST_PLOTPROPSTRING

%% Test 1.1: Instantiation - empty

pr = PlotPropString();

prop_number = PlotPropString.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropString.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropString:' BRAPH2.BUG_FUNC], ...
		['PlotPropString.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropString.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropString:' BRAPH2.BUG_FUNC], ...
		['PlotPropString.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropString.getr(''' TAG ''').'] ...
		)
end

