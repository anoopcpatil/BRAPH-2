%TEST_PLOTPROPMATRIX

%% Test 1.1: Instantiation - empty

pr = PlotPropMatrix();

prop_number = PlotPropMatrix.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropMatrix.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
		['PlotPropMatrix.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropMatrix.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
		['PlotPropMatrix.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropMatrix.getr(''' TAG ''').'] ...
		)
end

