%% Test 3: Result

warning('off', 'BRAPH2:PlotPropMatrix')
pr = PlotPropMatrix( ...
	PlotPropMatrix.ID, PlotPropMatrix.getPropDefault(PlotPropMatrix.ID), ...
	PlotPropMatrix.BKGCOLOR, PlotPropMatrix.getPropDefault(PlotPropMatrix.BKGCOLOR), ...
	PlotPropMatrix.EL, PlotPropMatrix.getPropDefault(PlotPropMatrix.EL), ...
	PlotPropMatrix.PROP, PlotPropMatrix.getPropDefault(PlotPropMatrix.PROP), ...
	PlotPropMatrix.TITLE, PlotPropMatrix.getPropDefault(PlotPropMatrix.TITLE), ...
	PlotPropMatrix.ENABLE, PlotPropMatrix.getPropDefault(PlotPropMatrix.ENABLE), ...
	PlotPropMatrix.WAITBAR, PlotPropMatrix.getPropDefault(PlotPropMatrix.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropMatrix')

prop_number = PlotPropMatrix.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropMatrix.getPropTag(prop));
	switch PlotPropMatrix.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
				['PlotPropMatrix.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
				['PlotPropMatrix.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
					['PlotPropMatrix.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropMatrix:' BRAPH2.BUG_FUNC], ...
					['PlotPropMatrix.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

