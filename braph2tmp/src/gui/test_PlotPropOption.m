%% Test 3: Result

warning('off', 'BRAPH2:PlotPropOption')
pr = PlotPropOption( ...
	PlotPropOption.ID, PlotPropOption.getPropDefault(PlotPropOption.ID), ...
	PlotPropOption.BKGCOLOR, PlotPropOption.getPropDefault(PlotPropOption.BKGCOLOR), ...
	PlotPropOption.EL, PlotPropOption.getPropDefault(PlotPropOption.EL), ...
	PlotPropOption.PROP, PlotPropOption.getPropDefault(PlotPropOption.PROP), ...
	PlotPropOption.TITLE, PlotPropOption.getPropDefault(PlotPropOption.TITLE), ...
	PlotPropOption.ENABLE, PlotPropOption.getPropDefault(PlotPropOption.ENABLE), ...
	PlotPropOption.WAITBAR, PlotPropOption.getPropDefault(PlotPropOption.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropOption')

prop_number = PlotPropOption.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropOption.getPropTag(prop));
	switch PlotPropOption.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
				['PlotPropOption.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
				['PlotPropOption.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
					['PlotPropOption.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
					['PlotPropOption.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

