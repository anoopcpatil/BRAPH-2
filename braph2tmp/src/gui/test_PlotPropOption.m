%TEST_PLOTPROPOPTION

%% Test 1.1: Instantiation - empty

pr = PlotPropOption();

prop_number = PlotPropOption.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropOption.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
		['PlotPropOption.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropOption.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropOption:' BRAPH2.BUG_FUNC], ...
		['PlotPropOption.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropOption.getr(''' TAG ''').'] ...
		)
end

