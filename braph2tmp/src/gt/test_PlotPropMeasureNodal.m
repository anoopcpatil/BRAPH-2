	PlotPropMeasureNodal.TITLE, PlotPropMeasureNodal.getPropDefault(PlotPropMeasureNodal.TITLE), ...
	PlotPropMeasureNodal.ENABLE, PlotPropMeasureNodal.getPropDefault(PlotPropMeasureNodal.ENABLE), ...
	PlotPropMeasureNodal.WAITBAR, PlotPropMeasureNodal.getPropDefault(PlotPropMeasureNodal.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropMeasureNodal')

prop_number = PlotPropMeasureNodal.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropMeasureNodal.getPropTag(prop));
	switch PlotPropMeasureNodal.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMeasureNodal:' BRAPH2.BUG_FUNC], ...
				['PlotPropMeasureNodal.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMeasureNodal:' BRAPH2.BUG_FUNC], ...
				['PlotPropMeasureNodal.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureNodal:' BRAPH2.BUG_FUNC], ...
					['PlotPropMeasureNodal.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureNodal:' BRAPH2.BUG_FUNC], ...
					['PlotPropMeasureNodal.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

