%% Test 3: Result

warning('off', 'BRAPH2:PlotPropMeasureGlobal')
pr = PlotPropMeasureGlobal( ...
	PlotPropMeasureGlobal.ID, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.ID), ...
	PlotPropMeasureGlobal.BKGCOLOR, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.BKGCOLOR), ...
	PlotPropMeasureGlobal.EL, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.EL), ...
	PlotPropMeasureGlobal.PROP, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.PROP), ...
	PlotPropMeasureGlobal.TITLE, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.TITLE), ...
	PlotPropMeasureGlobal.ENABLE, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.ENABLE), ...
	PlotPropMeasureGlobal.WAITBAR, PlotPropMeasureGlobal.getPropDefault(PlotPropMeasureGlobal.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropMeasureGlobal')

prop_number = PlotPropMeasureGlobal.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropMeasureGlobal.getPropTag(prop));
	switch PlotPropMeasureGlobal.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
				['PlotPropMeasureGlobal.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
				['PlotPropMeasureGlobal.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
					['PlotPropMeasureGlobal.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
					['PlotPropMeasureGlobal.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

