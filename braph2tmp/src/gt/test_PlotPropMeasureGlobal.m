%TEST_PLOTPROPMEASUREGLOBAL

%% Test 1.1: Instantiation - empty

pr = PlotPropMeasureGlobal();

prop_number = PlotPropMeasureGlobal.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropMeasureGlobal.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
		['PlotPropMeasureGlobal.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropMeasureGlobal.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropMeasureGlobal:' BRAPH2.BUG_FUNC], ...
		['PlotPropMeasureGlobal.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropMeasureGlobal.getr(''' TAG ''').'] ...
		)
end

