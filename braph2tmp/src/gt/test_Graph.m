%TEST_GRAPH

%% Test 1.1: Instantiation - empty

g = Graph();

prop_number = Graph.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Graph.getPropTag(prop));
	assert( ...
		isa(g.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
		['Graph.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Graph.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(g.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
		['Graph.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Graph.getr(''' TAG ''').'] ...
		)
end

