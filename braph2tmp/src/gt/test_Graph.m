				['Being a result, Graph.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
				'Or there could be an error in Graph.getr(''' TAG ''').'] ...
				)
	end
end

%% Test 2: Callbacks

warning('off', 'BRAPH2:Graph')
g_0 = Graph( ...
	Graph.ID, Graph.getPropDefault(Graph.ID), ...
	Graph.LABEL, Graph.getPropDefault(Graph.LABEL), ...
	Graph.NOTES, Graph.getPropDefault(Graph.NOTES), ...
	Graph.BRAINATLAS, Graph.getPropDefault(Graph.BRAINATLAS), ...
	Graph.A, Graph.getPropDefault(Graph.A), ...
	Graph.M_DICT, Graph.getPropDefault(Graph.M_DICT) ...
	);
warning('on', 'BRAPH2:Graph')

g_1 = Graph( ...
	Graph.ID, Callback('EL', g_0, 'PROP', 1), ...
	Graph.LABEL, Graph.getPropDefault(Graph.LABEL), ...
	Graph.NOTES, Graph.getPropDefault(Graph.NOTES), ...
	Graph.BRAINATLAS, Callback('EL', g_0, 'PROP', 4), ...
	Graph.A, NoValue(), ...
	Graph.M_DICT, NoValue() ...
	);
	
prop_number = Graph.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Graph.getPropTag(prop));
	switch Graph.getPropCategory(prop)
		case {Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
			assert( ...
				isa(g_1.getr(prop),  'Callback'), ...
				[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
				['Graph.getr(' int2str(prop) ') must be a callback.'] ...
				)
			assert( ...
				isequal(g_1.get(prop), g_0.get(prop)) && ~isa(g_1.get(prop), 'Callback'), ...
				[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
				['Graph.get(' int2str(prop) ') must be the actual property and NOT a callback.'] ...
				)
		case {Category.METADATA, Category.RESULT}
			assert( ...
				~isa(g_1.getr(prop), 'Callback'), ...
				[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
				['Graph.getr(' int2str(prop) ') must NOT be a callback.'] ...
