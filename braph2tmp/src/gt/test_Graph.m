%% Test 3: Result

warning('off', 'BRAPH2:Graph')
g = Graph( ...
	Graph.ID, Graph.getPropDefault(Graph.ID), ...
	Graph.LABEL, Graph.getPropDefault(Graph.LABEL), ...
	Graph.NOTES, Graph.getPropDefault(Graph.NOTES), ...
	Graph.BRAINATLAS, Graph.getPropDefault(Graph.BRAINATLAS), ...
	Graph.A, Graph.getPropDefault(Graph.A), ...
	Graph.M_DICT, Graph.getPropDefault(Graph.M_DICT) ...
	);
warning('on', 'BRAPH2:Graph')

prop_number = Graph.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Graph.getPropTag(prop));
	switch Graph.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(g.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
				['Graph.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(g.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
				['Graph.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(g.getPropSettings(prop))
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop)), ...
					[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
					['Graph.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					g.checkFormat(g.getPropFormat(prop), g.get(prop), g.getPropSettings(prop)), ...
					[BRAPH2.STR ':Graph:' BRAPH2.BUG_FUNC], ...
					['Graph.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

