%% Test 3: Result

warning('off', 'BRAPH2:PlotPropEmptyG')
pr = PlotPropEmptyG( ...
	PlotPropEmptyG.ID, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.ID), ...
	PlotPropEmptyG.BKGCOLOR, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.BKGCOLOR), ...
	PlotPropEmptyG.EL, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.EL), ...
	PlotPropEmptyG.PROP, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.PROP), ...
	PlotPropEmptyG.TITLE, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.TITLE), ...
	PlotPropEmptyG.ENABLE, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.ENABLE), ...
	PlotPropEmptyG.WAITBAR, PlotPropEmptyG.getPropDefault(PlotPropEmptyG.WAITBAR) ...
	);
warning('on', 'BRAPH2:PlotPropEmptyG')

prop_number = PlotPropEmptyG.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropEmptyG.getPropTag(prop));
	switch PlotPropEmptyG.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
				['PlotPropEmptyG.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
				['PlotPropEmptyG.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
					['PlotPropEmptyG.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
					['PlotPropEmptyG.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

