%TEST_PLOTPROPEMPTYG

%% Test 1.1: Instantiation - empty

pr = PlotPropEmptyG();

prop_number = PlotPropEmptyG.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotPropEmptyG.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
		['PlotPropEmptyG.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropEmptyG.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotPropEmptyG:' BRAPH2.BUG_FUNC], ...
		['PlotPropEmptyG.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotPropEmptyG.getr(''' TAG ''').'] ...
		)
end

