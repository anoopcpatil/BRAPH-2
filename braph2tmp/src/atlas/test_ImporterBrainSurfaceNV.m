for prop = 1:1:prop_number
	TAG = upper(ImporterBrainSurfaceNV.getPropTag(prop));
	switch ImporterBrainSurfaceNV.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(im.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':ImporterBrainSurfaceNV:' BRAPH2.BUG_FUNC], ...
				['ImporterBrainSurfaceNV.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(im.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':ImporterBrainSurfaceNV:' BRAPH2.BUG_FUNC], ...
				['ImporterBrainSurfaceNV.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(im.getPropSettings(prop))
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop)), ...
					[BRAPH2.STR ':ImporterBrainSurfaceNV:' BRAPH2.BUG_FUNC], ...
					['ImporterBrainSurfaceNV.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					im.checkFormat(im.getPropFormat(prop), im.get(prop), im.getPropSettings(prop)), ...
					[BRAPH2.STR ':ImporterBrainSurfaceNV:' BRAPH2.BUG_FUNC], ...
					['ImporterBrainSurfaceNV.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

