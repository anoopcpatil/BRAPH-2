%TEST_PLOTBRAINVIEW

%% Test 1.1: Instantiation - empty

pr = PlotBrainView();

prop_number = PlotBrainView.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PlotBrainView.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PlotBrainView:' BRAPH2.BUG_FUNC], ...
		['PlotBrainView.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotBrainView.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PlotBrainView:' BRAPH2.BUG_FUNC], ...
		['PlotBrainView.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PlotBrainView.getr(''' TAG ''').'] ...
		)
end

