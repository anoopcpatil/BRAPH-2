					ex.checkFormat(ex.getPropFormat(prop), ex.get(prop)), ...
					[BRAPH2.STR ':ExporterBrainAtlasTXT:' BRAPH2.BUG_FUNC], ...
					['ExporterBrainAtlasTXT.get(' int2str(prop) ') returns a value with the wrong format.'] ...
