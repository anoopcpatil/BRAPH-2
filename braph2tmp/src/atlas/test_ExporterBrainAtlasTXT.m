	[BRAPH2.STR ':ImporterBrainAtlasTXT:' BRAPH2.BUG_IO], ...
    'Problems saving or loading a brain atlas.')
assert(ba.get('BR_DICT').length() == ba_loaded.get('BR_DICT').length(), ...
	[BRAPH2.STR ':ImporterBrainAtlasTXT:' BRAPH2.BUG_IO], ...
    'Problems saving or loading a brain atlas.')
