%TEST_ETD2

%% Test 1.1: Instantiation - empty

et = ETD2();

prop_number = ETD2.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ETD2.getPropTag(prop));
	assert( ...
		isa(et.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
		['ETD2.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ETD2.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(et.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
		['ETD2.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ETD2.getr(''' TAG ''').'] ...
		)
end

