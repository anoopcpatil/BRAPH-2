%% Test 3: Result

warning('off', 'BRAPH2:ETD2')
et = ETD2( ...
	ETD2.UNO, ETD2.getPropDefault(ETD2.UNO), ...
	ETD2.DUE, ETD2.getPropDefault(ETD2.DUE), ...
	ETD2.TRE, ETD2.getPropDefault(ETD2.TRE) ...
	);
warning('on', 'BRAPH2:ETD2')

prop_number = ETD2.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ETD2.getPropTag(prop));
	switch ETD2.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(et.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
				['ETD2.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(et.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
				['ETD2.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(et.getPropSettings(prop))
				assert( ...
					et.checkFormat(et.getPropFormat(prop), et.get(prop)), ...
					[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
					['ETD2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					et.checkFormat(et.getPropFormat(prop), et.get(prop), et.getPropSettings(prop)), ...
					[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
					['ETD2.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

