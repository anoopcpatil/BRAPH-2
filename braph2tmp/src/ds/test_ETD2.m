%% Test 5: Multiple values

et1 = ETD2();
et1.set('UNO', 1)
assert_with_error("varargin{1}.set('DUE', 2)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
    et1)
assert_with_error("varargin{1}.set('TRE', 3)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
    et1)

et2 = ETD2();
et2.set('DUE', 2)
assert_with_error("varargin{1}.set('UNO', 1)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
    et2)
assert_with_error("varargin{1}.set('TRE', 3)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
    et2)

et3 = ETD2();
et3.set('TRE', 3)
assert_with_error("varargin{1}.set('UNO', 1)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
    et3)
assert_with_error("varargin{1}.set('DUE', 2)", ...
    [BRAPH2.STR ':ETD2:' BRAPH2.WRONG_INPUT], ...
