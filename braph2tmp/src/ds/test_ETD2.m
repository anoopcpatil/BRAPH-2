%% Test 2: Callbacks

warning('off', 'BRAPH2:ETD2')
et_0 = ETD2( ...
	ETD2.UNO, ETD2.getPropDefault(ETD2.UNO), ...
	ETD2.DUE, ETD2.getPropDefault(ETD2.DUE), ...
	ETD2.TRE, ETD2.getPropDefault(ETD2.TRE) ...
	);
warning('on', 'BRAPH2:ETD2')

et_1 = ETD2( ...
	ETD2.UNO, Callback('EL', et_0, 'PROP', 1), ...
	ETD2.DUE, Callback('EL', et_0, 'PROP', 2), ...
	ETD2.TRE, Callback('EL', et_0, 'PROP', 3) ...
	);
	
prop_number = ETD2.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ETD2.getPropTag(prop));
	switch ETD2.getPropCategory(prop)
		case {Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
			assert( ...
				isa(et_1.getr(prop),  'Callback'), ...
				[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
				['ETD2.getr(' int2str(prop) ') must be a callback.'] ...
				)
			assert( ...
				isequal(et_1.get(prop), et_0.get(prop)) && ~isa(et_1.get(prop), 'Callback'), ...
				[BRAPH2.STR ':ETD2:' BRAPH2.BUG_FUNC], ...
				['ETD2.get(' int2str(prop) ') must be the actual property and NOT a callback.'] ...
