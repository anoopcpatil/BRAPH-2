%% Test 1.1: Instantiation - empty

et = ETC3();

prop_number = ETC3.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(ETC3.getPropTag(prop));
	assert( ...
		isa(et.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':ETC3:' BRAPH2.BUG_FUNC], ...
		['ETC3.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ETC3.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(et.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':ETC3:' BRAPH2.BUG_FUNC], ...
		['ETC3.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in ETC3.getr(''' TAG ''').'] ...
		)
end

