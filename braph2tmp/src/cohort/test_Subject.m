			assert( ...
				~isa(sub_1.getr(prop), 'Callback'), ...
				[BRAPH2.STR ':Subject:' BRAPH2.BUG_FUNC], ...
				['Subject.getr(' int2str(prop) ') must NOT be a callback.'] ...
