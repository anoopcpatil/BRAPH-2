%TEST_SUBJECT

%% Test 1.1: Instantiation - empty

sub = Subject();

prop_number = Subject.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Subject.getPropTag(prop));
	assert( ...
		isa(sub.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':Subject:' BRAPH2.BUG_FUNC], ...
		['Subject.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Subject.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(sub.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':Subject:' BRAPH2.BUG_FUNC], ...
		['Subject.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in Subject.getr(''' TAG ''').'] ...
		)
end

