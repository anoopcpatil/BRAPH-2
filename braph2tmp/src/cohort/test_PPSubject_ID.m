%TEST_PPSUBJECT_ID

%% Test 1.1: Instantiation - empty

pr = PPSubject_ID();

prop_number = PPSubject_ID.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPSubject_ID.getPropTag(prop));
	assert( ...
		isa(pr.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PPSubject_ID:' BRAPH2.BUG_FUNC], ...
		['PPSubject_ID.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPSubject_ID.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pr.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PPSubject_ID:' BRAPH2.BUG_FUNC], ...
		['PPSubject_ID.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPSubject_ID.getr(''' TAG ''').'] ...
		)
end

