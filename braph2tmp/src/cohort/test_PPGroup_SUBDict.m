	PPGroup_SUBDict.ENABLE, PPGroup_SUBDict.getPropDefault(PPGroup_SUBDict.ENABLE), ...
	PPGroup_SUBDict.WAITBAR, PPGroup_SUBDict.getPropDefault(PPGroup_SUBDict.WAITBAR) ...
	);
warning('on', 'BRAPH2:PPGroup_SUBDict')

prop_number = PPGroup_SUBDict.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPGroup_SUBDict.getPropTag(prop));
	switch PPGroup_SUBDict.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pr.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPGroup_SUBDict:' BRAPH2.BUG_FUNC], ...
				['PPGroup_SUBDict.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pr.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPGroup_SUBDict:' BRAPH2.BUG_FUNC], ...
				['PPGroup_SUBDict.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pr.getPropSettings(prop))
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop)), ...
					[BRAPH2.STR ':PPGroup_SUBDict:' BRAPH2.BUG_FUNC], ...
					['PPGroup_SUBDict.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pr.checkFormat(pr.getPropFormat(prop), pr.get(prop), pr.getPropSettings(prop)), ...
					[BRAPH2.STR ':PPGroup_SUBDict:' BRAPH2.BUG_FUNC], ...
					['PPGroup_SUBDict.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

