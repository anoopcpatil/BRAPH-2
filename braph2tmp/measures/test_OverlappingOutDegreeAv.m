				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':OverlappingOutDegreeAv:' BRAPH2.BUG_FUNC], ...
					['OverlappingOutDegreeAv.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':OverlappingOutDegreeAv:' BRAPH2.BUG_FUNC], ...
