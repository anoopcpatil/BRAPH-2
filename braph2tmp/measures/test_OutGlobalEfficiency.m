	OutGlobalEfficiency.M, OutGlobalEfficiency.getPropDefault(OutGlobalEfficiency.M) ...
	);
warning('on', 'BRAPH2:OutGlobalEfficiency')

prop_number = OutGlobalEfficiency.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(OutGlobalEfficiency.getPropTag(prop));
	switch OutGlobalEfficiency.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':OutGlobalEfficiency:' BRAPH2.BUG_FUNC], ...
				['OutGlobalEfficiency.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':OutGlobalEfficiency:' BRAPH2.BUG_FUNC], ...
				['OutGlobalEfficiency.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':OutGlobalEfficiency:' BRAPH2.BUG_FUNC], ...
					['OutGlobalEfficiency.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':OutGlobalEfficiency:' BRAPH2.BUG_FUNC], ...
					['OutGlobalEfficiency.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

