				[BRAPH2.STR ':OutDegreeAv:' BRAPH2.BUG_FUNC], ...
				['OutDegreeAv.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':OutDegreeAv:' BRAPH2.BUG_FUNC], ...
				['OutDegreeAv.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':OutDegreeAv:' BRAPH2.BUG_FUNC], ...
					['OutDegreeAv.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':OutDegreeAv:' BRAPH2.BUG_FUNC], ...
					['OutDegreeAv.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

