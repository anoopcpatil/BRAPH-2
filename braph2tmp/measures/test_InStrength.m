m_1 = InStrength( ...
	InStrength.ID, Callback('EL', m_0, 'PROP', 1), ...
	InStrength.LABEL, InStrength.getPropDefault(InStrength.LABEL), ...
	InStrength.NOTES, InStrength.getPropDefault(InStrength.NOTES), ...
	InStrength.G, Callback('EL', m_0, 'PROP', 4), ...
	InStrength.M, NoValue() ...
	);
	
prop_number = InStrength.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(InStrength.getPropTag(prop));
	switch InStrength.getPropCategory(prop)
		case {Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
			assert( ...
				isa(m_1.getr(prop),  'Callback'), ...
				[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
				['InStrength.getr(' int2str(prop) ') must be a callback.'] ...
