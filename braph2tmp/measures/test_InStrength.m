%% Test 5: GraphWD

B = [
    0  .2 1
    0  0  .8
    1  0  0
    ];
known_in_strength = {[1 0.2 1.8]'};
g = GraphWD('B', B);
s_outside_g = InStrength('G', g);

assert(isequal(s_outside_g.get('M'), known_in_strength), ...
    [BRAPH2.STR ':InStrength:' BRAPH2.BUG_ERR], ...
    'InStrength is not being calculated correctly for GraphWD.')

