%% Test 1.1: Instantiation - empty

m = InStrength();

prop_number = InStrength.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(InStrength.getPropTag(prop));
	assert( ...
		isa(m.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
		['InStrength.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in InStrength.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(m.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
		['InStrength.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in InStrength.getr(''' TAG ''').'] ...
		)
end

