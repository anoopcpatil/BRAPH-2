warning('off', 'BRAPH2:InStrength')
m = InStrength( ...
	InStrength.ID, InStrength.getPropDefault(InStrength.ID), ...
	InStrength.LABEL, InStrength.getPropDefault(InStrength.LABEL), ...
	InStrength.NOTES, InStrength.getPropDefault(InStrength.NOTES), ...
