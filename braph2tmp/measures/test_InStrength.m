	InStrength.M, InStrength.getPropDefault(InStrength.M) ...
	);
warning('on', 'BRAPH2:InStrength')

prop_number = InStrength.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(InStrength.getPropTag(prop));
	switch InStrength.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
				['InStrength.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
				['InStrength.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
					['InStrength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':InStrength:' BRAPH2.BUG_FUNC], ...
					['InStrength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

