%% Test 3: Result

warning('off', 'BRAPH2:GlobalEfficiency')
m = GlobalEfficiency( ...
	GlobalEfficiency.ID, GlobalEfficiency.getPropDefault(GlobalEfficiency.ID), ...
	GlobalEfficiency.LABEL, GlobalEfficiency.getPropDefault(GlobalEfficiency.LABEL), ...
	GlobalEfficiency.NOTES, GlobalEfficiency.getPropDefault(GlobalEfficiency.NOTES), ...
	GlobalEfficiency.G, GlobalEfficiency.getPropDefault(GlobalEfficiency.G), ...
	GlobalEfficiency.M, GlobalEfficiency.getPropDefault(GlobalEfficiency.M) ...
	);
warning('on', 'BRAPH2:GlobalEfficiency')

prop_number = GlobalEfficiency.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(GlobalEfficiency.getPropTag(prop));
	switch GlobalEfficiency.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':GlobalEfficiency:' BRAPH2.BUG_FUNC], ...
				['GlobalEfficiency.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':GlobalEfficiency:' BRAPH2.BUG_FUNC], ...
				['GlobalEfficiency.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':GlobalEfficiency:' BRAPH2.BUG_FUNC], ...
					['GlobalEfficiency.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':GlobalEfficiency:' BRAPH2.BUG_FUNC], ...
					['GlobalEfficiency.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

