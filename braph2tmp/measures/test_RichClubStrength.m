	RichClubStrength.M, RichClubStrength.getPropDefault(RichClubStrength.M), ...
	RichClubStrength.PARAMETRIC_VALUE, RichClubStrength.getPropDefault(RichClubStrength.PARAMETRIC_VALUE) ...
	);
warning('on', 'BRAPH2:RichClubStrength')

prop_number = RichClubStrength.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(RichClubStrength.getPropTag(prop));
	switch RichClubStrength.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':RichClubStrength:' BRAPH2.BUG_FUNC], ...
				['RichClubStrength.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':RichClubStrength:' BRAPH2.BUG_FUNC], ...
				['RichClubStrength.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':RichClubStrength:' BRAPH2.BUG_FUNC], ...
					['RichClubStrength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':RichClubStrength:' BRAPH2.BUG_FUNC], ...
					['RichClubStrength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

