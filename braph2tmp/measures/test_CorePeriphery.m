	CorePeriphery.ID, CorePeriphery.getPropDefault(CorePeriphery.ID), ...
	CorePeriphery.LABEL, CorePeriphery.getPropDefault(CorePeriphery.LABEL), ...
	CorePeriphery.NOTES, CorePeriphery.getPropDefault(CorePeriphery.NOTES), ...
