	StrengthAv.ID, Callback('EL', m_0, 'PROP', 1), ...
	StrengthAv.LABEL, StrengthAv.getPropDefault(StrengthAv.LABEL), ...
	StrengthAv.NOTES, StrengthAv.getPropDefault(StrengthAv.NOTES), ...
	StrengthAv.G, Callback('EL', m_0, 'PROP', 4), ...
	StrengthAv.M, NoValue() ...
	);
	
prop_number = StrengthAv.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(StrengthAv.getPropTag(prop));
	switch StrengthAv.getPropCategory(prop)
		case {Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
			assert( ...
				isa(m_1.getr(prop),  'Callback'), ...
				[BRAPH2.STR ':StrengthAv:' BRAPH2.BUG_FUNC], ...
