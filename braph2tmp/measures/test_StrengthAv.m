%% Test 5: GraphWU

B = [
    0  .2 1
    .2 0  0
    1  0  0
    ];
known_strength_av = {mean([1.2 0.2 1])'};
g = GraphWU('B', B);
s_outside_g = StrengthAv('G', g);

assert(isequal(s_outside_g.get('M'), known_strength_av), ...
    [BRAPH2.STR ':Strength:' BRAPH2.BUG_ERR], ...
    'StrengthAV is not being calculated correctly for GraphWU.')

