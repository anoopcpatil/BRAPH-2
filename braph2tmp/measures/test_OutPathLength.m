	TAG = upper(OutPathLength.getPropTag(prop));
	switch OutPathLength.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':OutPathLength:' BRAPH2.BUG_FUNC], ...
				['OutPathLength.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':OutPathLength:' BRAPH2.BUG_FUNC], ...
				['OutPathLength.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':OutPathLength:' BRAPH2.BUG_FUNC], ...
					['OutPathLength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':OutPathLength:' BRAPH2.BUG_FUNC], ...
					['OutPathLength.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

