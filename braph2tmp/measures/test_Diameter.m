	Diameter.ID, Diameter.getPropDefault(Diameter.ID), ...
	Diameter.LABEL, Diameter.getPropDefault(Diameter.LABEL), ...
	Diameter.NOTES, Diameter.getPropDefault(Diameter.NOTES), ...
	Diameter.G, Diameter.getPropDefault(Diameter.G), ...
	Diameter.M, Diameter.getPropDefault(Diameter.M), ...
	Diameter.RULE, Diameter.getPropDefault(Diameter.RULE) ...
	);
warning('on', 'BRAPH2:Diameter')

prop_number = Diameter.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(Diameter.getPropTag(prop));
	switch Diameter.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(m.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':Diameter:' BRAPH2.BUG_FUNC], ...
				['Diameter.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(m.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':Diameter:' BRAPH2.BUG_FUNC], ...
				['Diameter.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':Diameter:' BRAPH2.BUG_FUNC], ...
					['Diameter.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':Diameter:' BRAPH2.BUG_FUNC], ...
					['Diameter.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

