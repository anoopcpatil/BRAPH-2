%% Test 1.1: Instantiation - empty

m = CategoricalPersistence();

prop_number = CategoricalPersistence.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(CategoricalPersistence.getPropTag(prop));
	assert( ...
		isa(m.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':CategoricalPersistence:' BRAPH2.BUG_FUNC], ...
