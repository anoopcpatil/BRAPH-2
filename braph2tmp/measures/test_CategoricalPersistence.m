		isa(m.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':CategoricalPersistence:' BRAPH2.BUG_FUNC], ...
		['CategoricalPersistence.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in CategoricalPersistence.getr(''' TAG ''').'] ...
		)
end

