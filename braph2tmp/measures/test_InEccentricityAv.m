				[BRAPH2.STR ':InEccentricityAv:' BRAPH2.BUG_FUNC], ...
				['InEccentricityAv.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(m.getPropSettings(prop))
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop)), ...
					[BRAPH2.STR ':InEccentricityAv:' BRAPH2.BUG_FUNC], ...
					['InEccentricityAv.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					m.checkFormat(m.getPropFormat(prop), m.get(prop), m.getPropSettings(prop)), ...
					[BRAPH2.STR ':InEccentricityAv:' BRAPH2.BUG_FUNC], ...
					['InEccentricityAv.get(' int2str(prop) ') returns a value with the wrong format.'] ...
