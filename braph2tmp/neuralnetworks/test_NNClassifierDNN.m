				)
			assert( ...
				isa(nn.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':NNClassifierDNN:' BRAPH2.BUG_FUNC], ...
				['NNClassifierDNN.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(nn.getPropSettings(prop))
				assert( ...
					nn.checkFormat(nn.getPropFormat(prop), nn.get(prop)), ...
					[BRAPH2.STR ':NNClassifierDNN:' BRAPH2.BUG_FUNC], ...
					['NNClassifierDNN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					nn.checkFormat(nn.getPropFormat(prop), nn.get(prop), nn.getPropSettings(prop)), ...
					[BRAPH2.STR ':NNClassifierDNN:' BRAPH2.BUG_FUNC], ...
					['NNClassifierDNN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
