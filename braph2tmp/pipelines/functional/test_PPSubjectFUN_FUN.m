	PPSubjectFUN_FUN.PROP, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.PROP), ...
	PPSubjectFUN_FUN.TITLE, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.TITLE), ...
	PPSubjectFUN_FUN.ENABLE, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.ENABLE), ...
	PPSubjectFUN_FUN.WAITBAR, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.WAITBAR) ...
	);
warning('on', 'BRAPH2:PPSubjectFUN_FUN')

prop_number = PPSubjectFUN_FUN.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPSubjectFUN_FUN.getPropTag(prop));
	switch PPSubjectFUN_FUN.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pl.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
				['PPSubjectFUN_FUN.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pl.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
				['PPSubjectFUN_FUN.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pl.getPropSettings(prop))
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop)), ...
					[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
					['PPSubjectFUN_FUN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop), pl.getPropSettings(prop)), ...
					[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
					['PPSubjectFUN_FUN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

