%% Test 3: Result

warning('off', 'BRAPH2:PPSubjectFUN_FUN')
pl = PPSubjectFUN_FUN( ...
	PPSubjectFUN_FUN.ID, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.ID), ...
	PPSubjectFUN_FUN.BKGCOLOR, PPSubjectFUN_FUN.getPropDefault(PPSubjectFUN_FUN.BKGCOLOR), ...
