%TEST_PPSUBJECTFUN_FUN

%% Test 1.1: Instantiation - empty

pl = PPSubjectFUN_FUN();

prop_number = PPSubjectFUN_FUN.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPSubjectFUN_FUN.getPropTag(prop));
	assert( ...
		isa(pl.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
		['PPSubjectFUN_FUN.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPSubjectFUN_FUN.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pl.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PPSubjectFUN_FUN:' BRAPH2.BUG_FUNC], ...
		['PPSubjectFUN_FUN.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPSubjectFUN_FUN.getr(''' TAG ''').'] ...
		)
end

