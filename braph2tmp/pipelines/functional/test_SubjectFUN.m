		isa(sub.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
		['SubjectFUN.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in SubjectFUN.getr(''' TAG ''').'] ...
		)
end

