%% Test 1.1: Instantiation - empty

sub = SubjectFUN();

prop_number = SubjectFUN.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(SubjectFUN.getPropTag(prop));
	assert( ...
		isa(sub.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
		['SubjectFUN.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in SubjectFUN.getr(' int2str(prop) ').'] ...
		)
