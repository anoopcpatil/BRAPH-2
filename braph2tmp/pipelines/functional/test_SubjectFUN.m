%% Test 3: Result

warning('off', 'BRAPH2:SubjectFUN')
sub = SubjectFUN( ...
	SubjectFUN.ID, SubjectFUN.getPropDefault(SubjectFUN.ID), ...
	SubjectFUN.LABEL, SubjectFUN.getPropDefault(SubjectFUN.LABEL), ...
	SubjectFUN.NOTES, SubjectFUN.getPropDefault(SubjectFUN.NOTES), ...
	SubjectFUN.BA, SubjectFUN.getPropDefault(SubjectFUN.BA), ...
	SubjectFUN.FUN, SubjectFUN.getPropDefault(SubjectFUN.FUN), ...
	SubjectFUN.AGE, SubjectFUN.getPropDefault(SubjectFUN.AGE), ...
	SubjectFUN.SEX, SubjectFUN.getPropDefault(SubjectFUN.SEX) ...
	);
warning('on', 'BRAPH2:SubjectFUN')

prop_number = SubjectFUN.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(SubjectFUN.getPropTag(prop));
	switch SubjectFUN.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(sub.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
				['SubjectFUN.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(sub.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
				['SubjectFUN.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(sub.getPropSettings(prop))
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop)), ...
					[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
					['SubjectFUN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop), sub.getPropSettings(prop)), ...
					[BRAPH2.STR ':SubjectFUN:' BRAPH2.BUG_FUNC], ...
					['SubjectFUN.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

