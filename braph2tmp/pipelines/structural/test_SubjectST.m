	SubjectST.ST, SubjectST.getPropDefault(SubjectST.ST), ...
	SubjectST.AGE, SubjectST.getPropDefault(SubjectST.AGE), ...
	SubjectST.SEX, SubjectST.getPropDefault(SubjectST.SEX) ...
	);
warning('on', 'BRAPH2:SubjectST')

prop_number = SubjectST.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(SubjectST.getPropTag(prop));
	switch SubjectST.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(sub.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
				['SubjectST.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(sub.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
				['SubjectST.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(sub.getPropSettings(prop))
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop)), ...
					[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
					['SubjectST.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					sub.checkFormat(sub.getPropFormat(prop), sub.get(prop), sub.getPropSettings(prop)), ...
					[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
					['SubjectST.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

