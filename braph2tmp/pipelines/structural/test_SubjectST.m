%% Test 1.1: Instantiation - empty

sub = SubjectST();

prop_number = SubjectST.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(SubjectST.getPropTag(prop));
	assert( ...
		isa(sub.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
		['SubjectST.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in SubjectST.getr(' int2str(prop) ').'] ...
		)
