	TAG = upper(SubjectST.getPropTag(prop));
	switch SubjectST.getPropCategory(prop)
		case {Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
			assert( ...
				isa(sub_1.getr(prop),  'Callback'), ...
