		isa(sub.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':SubjectST:' BRAPH2.BUG_FUNC], ...
		['SubjectST.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
		'Or there could be an error in SubjectST.getr(''' TAG ''').'] ...
		)
end

