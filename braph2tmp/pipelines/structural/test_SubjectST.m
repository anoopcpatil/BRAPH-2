	SubjectST.ID, SubjectST.getPropDefault(SubjectST.ID), ...
	SubjectST.LABEL, SubjectST.getPropDefault(SubjectST.LABEL), ...
	SubjectST.NOTES, SubjectST.getPropDefault(SubjectST.NOTES), ...
