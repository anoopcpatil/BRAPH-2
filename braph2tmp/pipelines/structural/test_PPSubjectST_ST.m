	switch PPSubjectST_ST.getPropCategory(prop)
		case {Category.METADATA, Category.PARAMETER, Category.DATA, Category.FIGURE, Category.GUI}
		case Category.RESULT
			assert( ...
				~isa(pl.get(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
				['PPSubjectST_ST.get(' int2str(prop) ') must NOT be a NoValue, because it should have been calculated.'] ...
				)
			assert( ...
				isa(pl.getr(prop),  'NoValue'), ...
				[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
				['PPSubjectST_ST.getr(' int2str(prop) ') must be a NoValue, because it has not been memorized.'] ...
				)
			if isempty(pl.getPropSettings(prop))
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop)), ...
					[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
					['PPSubjectST_ST.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			else
				assert( ...
					pl.checkFormat(pl.getPropFormat(prop), pl.get(prop), pl.getPropSettings(prop)), ...
					[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
					['PPSubjectST_ST.get(' int2str(prop) ') returns a value with the wrong format.'] ...
					)
			end
	end
end

