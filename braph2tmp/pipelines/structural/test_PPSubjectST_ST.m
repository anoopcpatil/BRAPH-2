%% Test 1.1: Instantiation - empty

pl = PPSubjectST_ST();

prop_number = PPSubjectST_ST.getPropNumber();
for prop = 1:1:prop_number
	TAG = upper(PPSubjectST_ST.getPropTag(prop));
	assert( ...
		isa(pl.getr(prop), 'NoValue'), ...
		[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
		['PPSubjectST_ST.getr(' int2str(prop) ') must be inizialized to NoValue(). ' ...
		'Or there could be an error in PPSubjectST_ST.getr(' int2str(prop) ').'] ...
		)
	assert( ...
		isa(pl.getr(TAG), 'NoValue'), ...
		[BRAPH2.STR ':PPSubjectST_ST:' BRAPH2.BUG_FUNC], ...
		['PPSubjectST_ST.getr(''' TAG ''') must be inizialized to NoValue(). ' ...
