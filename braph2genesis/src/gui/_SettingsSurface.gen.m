%%%% ¡default!
[0 0 0]

%%% ¡prop!
EDGEALPHA (figure, alpha) is the edge transparency.
%%%% ¡default!
0

%%% ¡prop!
FACECOLOR (figure, color) is the RGB face color.
%%%% ¡default!
[.5 .5 .5]

%%% ¡prop!
FACEALPHA (figure, alpha) is the face transparency.
%%%% ¡default!
1