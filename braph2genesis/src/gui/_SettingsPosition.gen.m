%% ¡header!
SettingsPosition < Element (st, position settings) contains the figure panel settings.

%%% ¡description!
% % %

%%% ¡seealso!
PanelFig, GUIFig

%% ¡props!

%%% ¡prop!
ID (data, string) is a few-letter code for the figure panel settings.

%%% ¡prop!
LABEL (metadata, string) is an extended label of the figure panel settings.

%%% ¡prop!
NOTES (metadata, string) are some specific notes about the figure panel settings.

%%% ¡prop!
AUTOPOS (figure, logical) determines whether the position is adjusted automatically.
%%%% ¡default!
true

%%% ¡prop!
X0 (figure, size) is the lower left x-coordinate of the panel in pixels.
%%%% ¡default!
1

%%% ¡prop!
Y0 (figure, size) is the lower-left y-coordinate of the panel in pixels.
%%%% ¡default!
1

%%% ¡prop!
WIDTH (figure, size) is the width of the panel in pixels.
%%%% ¡default!
800

%%% ¡prop!
HEIGHT (figure, size) is the height of the panel in pixels.
%%%% ¡default!
600