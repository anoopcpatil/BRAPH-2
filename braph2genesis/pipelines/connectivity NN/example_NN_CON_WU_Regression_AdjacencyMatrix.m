%% EXAMPLE_NN_CON_WU_REGRESSION_ADJACENCYMATRIX
% Script example pipeline for NN regression with the input of adjacency matrix 

clear variables %#ok<*NASGU>

%% Load brain atlas
im_ba = ImporterBrainAtlasXLS( ...
    'FILE', [fileparts(which('example_CON_WU')) filesep 'example data CON (DTI)' filesep 'desikan_atlas.xlsx'], ...
    'WAITBAR', true ...
    );

ba = im_ba.get('BA');

%% Load group of SubjectCON
im_gr = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_CON_WU')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr = im_gr.get('GR');

%% Construct the dataset
nnd = NNData_CON_WU( ...
    'GR', gr, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', 'age', ...
    'WAITBAR', true ...
    );

gr_nn = nnd.get('GR_NN');

%% Split the dataset
nnds = NNRegressorDataSplit( ...
    'GR', gr_nn, ...
    'SPLIT', 0.2, ...
    'FEATURE_MASK', 0.05, ...
    'WAITBAR', true ...
    );

gr_train = nnds.get('GR_TRAIN_FS');
gr_val = nnds.get('GR_VAL_FS');

%% Train the neural network regressor with the training set
regressor = NNRegressorDNN( ...
    'GR', gr_train, ...
    'VERBOSE', false, ...
    'PLOT_TRAINING', false, ...
    'SHUFFLE', 'every-epoch' ...
    );

regressor.memorize('MODEL');

%% Evaluate the regressor for the training set
nne_train = NNRegressorEvaluator( ...
    'GR', gr_train, ...
    'NN', regressor ...
    );

rmse_train = nne_train.get('RMSE');

%% Evaluate the regressor for the validation set
nne_val = NNRegressorEvaluator( ...
    'GR', gr_val, ...
    'NN', regressor ...
    );

rmse_val = nne_val.get('RMSE');

%% Load another group of subjectCON
im_gr = ImporterGroupSubjectCON_XLS( ...
    'DIRECTORY', [fileparts(which('example_NN_CON_WU_Regression_AdjacencyMatrix')) filesep 'example data CON (DTI)' filesep 'xls' filesep 'GroupName1'], ...
    'BA', ba, ...
    'WAITBAR', true ...
    );

gr_unseen = im_gr.get('GR');

%% Construct the dataset
nnd_unseen = NNData_CON_WU( ...
    'GR', gr_unseen, ...
    'INPUT_TYPE', 'adjacency_matrices', ...
    'TARGET_NAME', 'age', ...
    'WAITBAR', true ...
    );

gr_nn_unseen = nnd_unseen.get('GR_NN');

%% Evaluate the regressor for the testing set
nnds_test = NNRegressorDataSplit( ...
    'GR', gr_nn_unseen, ...
    'SPLIT', 1.0, ...
    'FEATURE_MASK', nnds.get('FEATURE_SELECTION_ANALYSIS'), ...
    'WAITBAR', true ...
    );

gr_test = nnds_test.get('GR_VAL_FS');

nne_test = NNRegressorEvaluator( ...
    'GR', gr_test, ...
    'NN', regressor ...
    );

rmse_test = nne_test.get('RMSE');

close all